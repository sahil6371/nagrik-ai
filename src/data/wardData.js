// GPS coordinates se nearest ward detect karo
export const detectWard = (lat, lng) => {
  let nearest = mumbaiWards[0]
  let minDist = Infinity

  for (const ward of mumbaiWards) {
    const dist = Math.sqrt(
      Math.pow(lat - ward.lat, 2) + Math.pow(lng - ward.lng, 2)
    )
    if (dist < minDist) {
      minDist = dist
      nearest = ward
    }
  }

  return nearest
}